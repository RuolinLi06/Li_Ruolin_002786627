package model;

import java.util.Date;

/**
 *
 * @author Ruolin Li
 * Degree 
 * Degree Start
 * Degree End
 */
public class Degree {
    
    private String name;
    //mm/yyyy
    private String start;
    private String end;

    public Degree(String name, String start, String end) {
        this.name = name;
        this.start = start;
        this.end = end;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }
    
}
